      <p>The duration of celebrations varied throughout history. Nowadays, the festival technically isn’t a national holiday, so there aren’t any days off. The best period for Lantern Festival lovers would be the Ming dynasty. It lasted around 1 month!</p>
      <p>There are some holidays that coincide with the Lantern Festival and are now celebrated together.</p>

      <h3>Torch Festival (火把节 / huǒ bǎ jié)</h3>
      <p>The Torch Festival began in ancient times to chase away insects and pests and pray for a good harvest.</p>
      <p>Children gather firewood and tree branches. Adults then light them. Holding these torches, entire communities dance in fields from dusk to dawn. Even now, it is still celebrated in some parts of Southwestern China.</p>

      <h3>Vegetable Stealing Festival (偷菜节 / tōu cài jié)</h3>
      <p>This is celebrated by the Miao ethnicity in China. On that day, groups of girls steal Chinese cabbage from someone’s fields. Being caught is totally fine, but it’s forbidden to steal from relatives or friends.</p>
      <p>Later, everyone puts the stolen Chinese cabbage together to make a feast. The one who eats the most will be the one to marry first.</p>
