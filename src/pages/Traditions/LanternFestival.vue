      <p>In the early days, the Lantern Festival was also called Shang Yuan (上元). It was dedicated to the heavenly palace (天宫 / tiān gōng). People would gather in their yards and give offerings to the gods. The lit lantern near the setup represented a god’s seat.</p>
      <p>Throughout history, countless variations of lanterns have been created. They can be small globes that fit in your palm, or as large as a parade float. People make lanterns in symbolic designs as well.</p>
