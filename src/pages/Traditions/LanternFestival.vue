      <p>A famous variation is the Kongming lantern (孔明灯 / kǒng míng dēng). They represent hope, success and happiness. In the past, people used these lanterns to signify they were safe after an attack.Now, they are used for wishes.</p>
      <p>Also known as the sky lantern (天灯 / tiān dēng), it sounds similar to 添丁 (tiān ding), which means “adding children”. Lanterns would be gifted to newlyweds or couples without children to pass on the blessings. Pregnant women would receive a pair of small lanterns to wish health and safety on both mother and child.</p>
      <p>Some regions also burn lanterns to determine the gender of their future child from the shape of the ashes.</p>
