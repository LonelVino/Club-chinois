      <h2>Origins and Histories</h2>
      <p>The general consensus is that the festival began more than 2000 years ago in the Western Han dynasty.</p>
      <p>Emperor Wu (汉武帝 / hàn wǔ dì) designated this day for worship rituals for Taiyi (太一神 / tài yī shén), one of the universe’s sovereigns.</p>
      <p>Intense power play and unrest came after his reign. The new emperor was Emperor Wen (汉文帝 / hàn wén dì). To celebrate the return of peace, he made the 15th a national holiday. Every household would light candles and lanterns. It became known as 闹元宵 (nào yuán xiāo). “Nao” can be interpreted as having fun, or going wild with excitement.</p>
      <p>Emperor Ming of the later Eastern Han was a devout Buddhist. He heard that on the 15th, monks would light candles for the Buddha. He ordered the palace and temples to light candles, and for the citizens to hang lanterns.</p>
      <p>Both events combined and eventually developed into the Lantern Festival we know today.</p>
      <el-divider></el-divider>

